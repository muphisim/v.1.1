/*! \brief This function initialises the position, weights and contributions of the GPs for each element in the FEM domain. The FEM are fully defined (including the shape functions)
  @param[out] GPs Array with all GPs in the domain
  @param[in] nodes Array with all nodes in the domain
  @param[in] elements Array with all elements (or background cells) in the domain
  @param[in] ndim Dimension of the domain
  @param[in] nodesFEM Array with the labels that correspond to the FEM nodes in the nodes general array
  @param[in] elementsFEM Array with the labels that correspond to the FEM elements in the elements general array
*/
extern void
GPsDistributionFEM(vector<classGPs *> &GPs, vector<classNodes *> &nodes, vector<classElements *> &elements, int ndim,
                   vector<int> &nodesFEM, vector<int> &elementsFEM) {

    int ind = 0; // The actual number of the element
    for (int i = 0; i < elementsFEM.size(); i++) {
        ind = elementsFEM[i];
        elements[ind]->defineGPs(ind, GPs, nodes, ndim);
    }
};

/*! \brief This function initialises the position, weights and contributions of the GPs for each background cell in the MM domain. The shape functions are not defined in this call
  @param[out] GPs Array with all GPs in the domain
  @param[in] nodes Array with all nodes in the domain
  @param[in] elements Array with all elements (or background cells) in the domain
  @param[in] ndim Dimension of the domain
  @param[in] nodesMM Array with the labels that correspond to the MM nodes in the nodes general array
  @param[in] elementsMM Array with the labels that correspond to the MM background cells in the elements general array
  @param[out] mmGPs Array with all mmGPs in the MM domain
  @param[in] orderd Order for the numerical integration
*/
extern void
GPsDistributionMM(vector<classGPs *> &GPs, vector<classNodes *> &nodes, vector<classElements *> &elements, int ndim,
                  vector<int> &nodesMM, vector<int> &elementsMM, vector<classGPs *> &mmGPs, int orderd) {
    int ind = 0;
    for (int i = 0; i < elementsMM.size(); i++) {
        ind = elementsMM[i];
        elements[ind]->definePositionGPsMM(ind, GPs, nodes, ndim, orderd, mmGPs);
    }
}

